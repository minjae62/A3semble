export function getStorageTip(category: string, name?: string): string {
  if (name) {
    const hit = NAME_TIPS.find((t) => name.includes(t.match));
    if (hit) return hit.tip;
  }
  return (
    CATEGORY_TIPS[category] ??
    "밀폐 용기에 담아 냉장 보관하고 소비기한 안에 드세요."
  );
}
